void PrintArrayWithout0(int[,] matrixArray)
{
    for (int i = 0; i < matrixArray.GetLength(0); i++)
    {
        for (int j = 0; j < matrixArray.GetLength(1); j++)
        {
            //Console.Write($"{matrixArray[i, j]} ");
            if (matrixArray[i, j] == 0)
                Console.Write($"  ");
            else
                Console.Write($"+ ");
        }
        Console.WriteLine();
    }
}

void FillArray(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillArray(row - 1, col);
        FillArray(row, col - 1);
        FillArray(row + 1, col);
        FillArray(row, col + 1);
    }
}